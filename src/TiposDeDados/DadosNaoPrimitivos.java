package TiposDeDados;

public class DadosNaoPrimitivos {
    public static void main(String[] args) {

        /*
        * Ddados nao primitivos: String, Array, Class
        * Objetivo: Criar um ninka e atribuir dados a ele.
        * */

        String nome = "Naruto";
        String nomeUpperCase = nome.toUpperCase(); // toUpperCase Vai colocar tudo em CAPSLOCK
        System.out.println(nomeUpperCase);

        String aldeia = "Aldeia da folha";
        String aldeiatoLowerCase = aldeia.toLowerCase(); // toLowerCase deixa tudo em minnusculo
        System.out.println(aldeiatoLowerCase);

    }
}
